const myColor = ["Red", "Green", "White", "Black"];

const joinedWithComma = myColor.join(',');
const joinedWithPlus = myColor.join('+');

console.log(joinedWithComma);  // Expected Output: "Red,Green,White,Black"
console.log(joinedWithComma);   // Expected Output: "Red,Green,White,Black"
console.log(joinedWithPlus);    // Expected Output: "Red+Green+White+Black"
